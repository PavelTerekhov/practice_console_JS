function getMathResult(BaseNumber, RepeatNumber) {
    if (typeof(RepeatNumber) !== "number" || RepeatNumber <= 0) {
        return (BaseNumber);
      } 

    let str = '';

    for (let i = 1; i <= RepeatNumber; i++) {
        if (i === RepeatNumber) {
            str += `${BaseNumber * i}`;
            // –¢—É—Ç –±–µ–∑ —á–µ—Ä—Ç–æ—á–µ–∫ –≤ –∫–æ–Ω—Ü–µ
        } else {
            str += `${BaseNumber * i}---`;
            // –≠—Ç–æ —Ç–æ–∂–µ —Å–∞–º–æ–µ, —á—Ç–æ –∏
            // str = str + num * i + "---"
        }
    }

    return str;

}

